  // ------- Routing graph, built once (lazily) from [footways] -------
  static List<LatLng>? _nodes;
  static Map<int, List<int>>? _adjacency;
  static List<List<int>>? _edges; // [a, b] endpoint indices (giant component)
  static Set<int>? _routable; // node indices in the largest component

  static String _key(LatLng p) =>
      '${p.latitude.toStringAsFixed(6)},${p.longitude.toStringAsFixed(6)}';

  static void _build() {
    final index = <String, int>{};
    final nodes = <LatLng>[];
    int idOf(LatLng p) {
      final k = _key(p);
      var i = index[k];
      if (i == null) {
        i = nodes.length;
        index[k] = i;
        nodes.add(p);
      }
      return i;
    }

    final adj = <int, Set<int>>{};
    for (final way in footways) {
      for (int i = 1; i < way.length; i++) {
        final a = idOf(way[i - 1]);
        final b = idOf(way[i]);
        if (a == b) continue;
        (adj[a] ??= <int>{}).add(b);
        (adj[b] ??= <int>{}).add(a);
      }
    }

    // Largest connected component (breadth-first).
    final seen = <int>{};
    Set<int> giant = <int>{};
    for (final start in adj.keys) {
      if (seen.contains(start)) continue;
      final comp = <int>{};
      final queue = <int>[start];
      seen.add(start);
      while (queue.isNotEmpty) {
        final x = queue.removeLast();
        comp.add(x);
        for (final y in adj[x] ?? const <int>{}) {
          if (seen.add(y)) queue.add(y);
        }
      }
      if (comp.length > giant.length) giant = comp;
    }

    final edges = <List<int>>[];
    for (final e in adj.entries) {
      if (!giant.contains(e.key)) continue;
      for (final n in e.value) {
        if (giant.contains(n) && e.key < n) edges.add(<int>[e.key, n]);
      }
    }

    _nodes = nodes;
    _adjacency = {for (final e in adj.entries) e.key: e.value.toList()};
    _edges = edges;
    _routable = giant;
  }

  /// De-duplicated junction/vertex nodes of the network.
  static List<LatLng> get nodes {
    if (_nodes == null) _build();
    return _nodes!;
  }

  /// Undirected adjacency (node index -> neighbour indices).
  static Map<int, List<int>> get adjacency {
    if (_adjacency == null) _build();
    return _adjacency!;
  }

  /// Routable lane segments as endpoint-index pairs (largest component only).
  static List<List<int>> get edges {
    if (_edges == null) _build();
    return _edges!;
  }

  /// Node indices that belong to the largest connected component.
  static Set<int> get routableNodes {
    if (_routable == null) _build();
    return _routable!;
  }
